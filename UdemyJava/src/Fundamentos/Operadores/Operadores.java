package Fundamentos.Operadores;

public class Operadores {
	public static void main(String[] args) {
		System.out.println(6-3);
		
		var a = 23.52;
		double b = 89.4;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a/b);
		System.out.println(a*b);
		
		int c = 65;
		int d = 23;
		
		System.out.println(c+d);
		System.out.println(c-d);
		System.out.println(c/d);
		System.out.println(c* (double) d);
	}
}
